#include<bits/stdc++.h>
using namespace std;
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> zigzagLevelOrder(TreeNode* root) {
        vector<vector<int>> ret;
        if(root == nullptr)
            return ret;
        queue<TreeNode *> que;
        que.push(root);
        int direction = 1;
        while(!que.empty()){
            int sz = que.size();
            vector<int> tmp;
            for (int i = 0; i < sz; i++)
            {
                TreeNode *node = que.front();
                que.pop();
                tmp.push_back(node->val);
                if(node->left)
                    que.push(node->left);
                if(node->right)
                    que.push(node->right);
                
            }
            if(direction == -1)
                reverse(tmp.begin(), tmp.end());
            ret.push_back(tmp);
            direction = -direction;
        }
        return ret;
    }
};
