// @lc code=start
#include<bits/stdc++.h>
using namespace std;
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:
    vector<vector<int>> levelOrder(TreeNode* root) {
        vector<vector<int>> ret;
        queue<TreeNode *> que;
        if(root == nullptr)
            return ret;
        que.push(root);
        while(!que.empty()){
            int sz = que.size();
            vector<int> tmp;
            for (int i = 0; i < sz; i++){
                TreeNode *tnode = que.front();
                que.pop();
                tmp.push_back(tnode->val);
                if(tnode->left)
                    que.push(tnode->left);
                if(tnode->right)
                    que.push(tnode->right);
            }
            ret.push_back(tmp);
        }
        return ret;
    }
};
